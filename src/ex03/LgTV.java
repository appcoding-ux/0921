package ex03;

public class LgTV extends TV{
	public void powerOn() {
		System.out.println("LgTV.. 전원 키다.");
	}
	public void powerOff() {
		System.out.println("LgTV.. 전원 끄다.");
	}
	public void volumUp() {
		System.out.println("LgTV.. 소리 올리다.");
	}
	public void volumDown() {
		System.out.println("LgTV.. 소리 내리다.");
	}
}